   function ValidCaptcha() {

      var mobile = $("#mobile").val();
      var name = $("#name").val();
      name = $.trim(name);
      var email = $("#email").val();
      email = $.trim(email);
      var state = $("#state").val();


      // here is the coding for validating the multiple file size //

