    (function($) {
        $(document).ready(function() {
            var $form = $('#oli-validate-form');
            var $ratingBox = $('.satisfied_customers_block');

            $form.addClass('single_smooth_form');

            var $step1Fields = $('.first_new_div_form').find('input, select, textarea').not('[type="hidden"]');
            var $step2Fields = $('.second_new_div_form').find('input, select, textarea');
            var $step3Fields = $('.three_new_div_form').find('input, select, textarea');
            var $step4Fields = $('.fourth_new_div_form').find('input, select, textarea');
            var $step5Fields = $('.fifth_new_div_form').find('input, select, textarea');

            function forceAnimationReady() {
                $('.first_new_div_form').css('display', 'block');

                $('.second_new_div_form, .three_new_div_form, .fourth_new_div_form, .fifth_new_div_form').each(function() {
                    this.style.setProperty('display', 'block', 'important');
                });
            }

            function removeStepClasses() {
                $form.removeClass(
                    'form_step_two_active form_step_three_active form_step_four_active form_step_five_active'
                );
            }

            function disableAllStepFields() {
                $step1Fields.prop('disabled', true);
                $step2Fields.prop('disabled', true);
                $step3Fields.prop('disabled', true);
                $step4Fields.prop('disabled', true);
                $step5Fields.prop('disabled', true);
            }

            function enableStep(stepNo) {
                disableAllStepFields();

                if (stepNo === 1) {
                    $step1Fields.prop('disabled', false);
                }

                if (stepNo === 2) {
                    $step2Fields.prop('disabled', false);
                }

                if (stepNo === 3) {
                    $step3Fields.prop('disabled', false);
                }

                if (stepNo === 4) {
                    $step4Fields.prop('disabled', false);
                }

                if (stepNo === 5) {
                    $step5Fields.prop('disabled', false);
                }
            }

            function goToStep(stepClass, stepNo) {
                forceAnimationReady();
                removeStepClasses();
                enableStep(stepNo);

                requestAnimationFrame(function() {
                    requestAnimationFrame(function() {
                        $form.addClass(stepClass);
                        $ratingBox.addClass('rating_show');
                    });
                });
            }

            /*
              Very important:
              Original backend click handler run korar age
              only current step field enable kore dicchi,
              so jQuery validation hidden/next fields niye block korbe na.
            */
            document.addEventListener('click', function(e) {
                if (e.target.closest('#nextBtn')) {
                    enableStep(1);
                }

                if (e.target.closest('#2ndSubmitBtn')) {
                    enableStep(2);
                }

                if (e.target.closest('#3rdSubmitBtn')) {
                    enableStep(3);
                }

                if (e.target.closest('#4thSubmitBtn')) {
                    enableStep(4);
                }

                if (e.target.closest('#5thSubmitBtn')) {
                    enableStep(5);
                }
            }, true);

            function getAjaxMode(settings) {
                var ajaxData = settings.data;

                if (typeof ajaxData === 'string') {
                    var params = new URLSearchParams(ajaxData);
                    return params.get('mode');
                }

                if (typeof ajaxData === 'object' && ajaxData !== null) {
                    return ajaxData.mode;
                }

                return null;
            }

            function getAjaxResponse(xhr) {
                if (xhr.responseJSON) {
                    return xhr.responseJSON;
                }

                try {
                    return JSON.parse(xhr.responseText);
                } catch (e) {
                    return null;
                }
            }

            /*
              Initial state:
              only first step fields enabled.
              Other fields disabled, so validation won't block first AJAX.
            */
            forceAnimationReady();
            enableStep(1);

            /*
              Backend AJAX success er porei animation step change hobe.
              So backend submit/AJAX untouched thakbe.
            */
            $(document).ajaxSuccess(function(event, xhr, settings) {
                if (!settings.url || settings.url.indexOf('submit_all_service_multi.php') === -1) {
                    return;
                }

                var response = getAjaxResponse(xhr);

                if (!response || !response.success) {
                    return;
                }

                var mode = getAjaxMode(settings);

                if (mode == '1') {
                    goToStep('form_step_two_active', 2);
                }

                if (mode == '2') {
                    goToStep('form_step_three_active', 3);
                }

                if (mode == '3') {
                    goToStep('form_step_four_active', 4);
                }

                if (mode == '4') {
                    goToStep('form_step_five_active', 5);
                }
            });
        });
    })(jQuery);
</script>
