   <div class="container custom_container">
      <div class="two_new_inner_block_holder">
         <h5>Steps to Convert LLP into a <span>Private Limited Company</span></h5>
      </div>
      <div class="two_text_block two_text_block_new">
         <p>The following steps are essential to follow for compliance for LLP to Private Limited company conversion:</p>
      </div>

      <div class="new_common_block">
         <h5>Consent of all Partners of the LLP</h5>
         <p>The process starts with a meeting among partners to get approval for converting the LLP they have to a Private Limited Company. According to section 366 of the Companies Act 2013, the approval of all partners is essential. Among all partners, only 2 partners need to be authorized to collect consent from all the partners for LLP conversion.</p>
      </div>


      <div class="new_common_block">
         <h5>Publication to the Newspaper</h5>
         <p>The intention of converting from LLP to a Pvt. Ltd Company must be disclosed in at least two popular newspapers. Among them, the advertisement must be one in a vernacular language newspaper and the other in the English daily. The announcement should be published for consecutive 21 days before filing URC-1.</p>
      </div>

      <div class="new_common_block">
         <h5>NOC required from ROC and Creditors</h5>
         <p>NOC or No Objection Certificate, is required from the Register of Companies and Creditors. Their NOC must be unconditional.</p>
      </div>

      <div class="new_common_block">
         <h5>Process Name Reservation of Company in RUN</h5>
         <p>RUN or Reserve Unique Name, is an e-form introduced by the Ministry Of Corporate Affairs. So, apply for name reservation for companies in RUN for the new private limited company. The application goes to the ROC.</p>
      </div>

      <div class="new_common_block">
         <h5>E-file URC-1, MOA, AOA, DIR-2 Forms and SPICe Plus</h5>
         <p>Within 30 days of the name reservation application of the conversion, you have to file URC-1 along with the SPICe Plus form, eMoA, eAoA, and DIR-2. ROC approves the forms and issues a new certificate for incorporation in the name of the company.</p>
      </div>



      <div class="new_common_block">
         <h5>List of Documents Required for Conversion of LLP into Company</h5>
