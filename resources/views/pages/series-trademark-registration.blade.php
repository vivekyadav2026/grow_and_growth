<script type="text/javascript">
   $(document).on('click', '#nextBtn', function(e) {
      e.preventDefault();

      var name = $('#name').val();
      var email = $('#email').val();
      var mobile = $('#mobile').val();
      var state = $('#state option:selected').val();
      var landing_uri = $('#landing_uri').val();
      var type = $('#type').val();

      if ($('.oli-validate-form').valid()) {

         $('#nextBtn').attr('disabled', true).css({
            'pointer-events': 'none',
            'opacity': '0.5'
         });
         $.ajax({
            url: "../../payment/submit_series_trademark.php",
            method: 'post',
            data: {
               name: name,
               email: email,
               mobile: mobile,
               state: state,
               landing_uri: landing_uri,
               type: type
            },
            dataType: "json",
            success: function(data) {
               if (data.success) {
                  $('.oli-validate-form')[0].reset();
                  $('#nextBtn').attr('disabled', false).val('Redirecting to Payment Lobby').css({
                     'pointer-events': 'all',
                     'opacity': 'all'
                  });
                  setTimeout(function() {
                     window.location.href = 'https://www.onlinelegalindia.com/payment/payment-lobby.php?oli-id=' + data.oli_id
                  }, 2000);
               } else {
                  alert(data.msg);
                  location.reload();
               }
            }
         });
      }
   });

