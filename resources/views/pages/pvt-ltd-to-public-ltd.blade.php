         <div class="common_heading">
            <h2>Procedure for Conversion into a <span>Public Limited Company</span></h2>
         </div>
         <div class="two_text_block two_text_block_new">
            <p class="two_p">The conversion of a Private Limited Company to a Public Limited Company has been done by following the Companies Act 2013 and Companies Incorporation Rules 2014.</p>
            <div class="new_h3">
               <h4>Notice issuing for a Board Meeting of Directors</h4>
               <p>A board meeting is conducted with directors. The proposed agenda needs to circulate and includes:</p>
               <div class="new_h3_inner">
                  <p>A. Company conversion-related Board Resolution Passing</p>
                  <p>B. Fixing a date, time, and venue for meeting with shareholders, if there is any</p>
                  <p>C. Approval of notice of EGM, and authorization from company directors or secretary for issuing the notice</p>
               </div>
            </div>
            <div class="new_h3">
               <h4>Board Meeting and Agenda Items Discussion</h4>
               <div class="new_h3_inner">
                  <p>A. Approval of the conversion from Pvt to Public Limited Company is also subject to the approval of shareholders. Arranging a meeting with them is also
                     mandatory.</p>
                  <p>B. Setting up a new MoA and AoA is required to get approval from the shareholders.</p>
                  <p>C. It is essential to fix a date, time, and venue for a general meeting</p>
               </div>
            </div>
            <div class="new_h3">
               <h4>General Meeting Notice Issuing</h4>
               <div class="new_h3_inner">
                  <p>A. 21 days before the general meeting, you have to issue a notice to all members.</p>
                  <p>B. If more than 95% of the members provide approval in electric mode, you can arrange the meeting on short notice.</p>
                  <p>C. The notice duration would be as per the Articles.</p>
               </div>
            </div>
            <div class="new_h3">
               <h4>Conducting a General Meeting</h4>
               <div class="new_h3_inner">
                  <p>A. The matter of discussion would be a resolution for the conversion of a private limited company to a public limited company.</p>
                  <p>B. The adoption of MoA (Memorandum of Association) and AoA (Articles of Association)</p>
               </div>
            </div>
            <div class="new_h3">
               <h4>File form MGT- 14</h4>
               <P>Fill out Form MGT-14 before 30 days of passing the special resolution. Let’s learn the attachments.</P>
            </div>
            <div class="new_h3">
               <h4>Attachments to Form MGT 14</h4>
               <div class="new_h3_ul_li">
                  <ul>
                     <li>Notice of EGM</li>
                     <li>Explanatory Statement</li>
                     <li>Certified copy of passed EGM Resolution</li>
                     <li>MOA Amended</li>
                     <li>AOA Amended</li>
                     <li>Notice of shorter EGM, if it is held after a short notice</li>
                  </ul>
               </div>
            </div>
            <div class="new_h3">
               <h4>File form: INC-27</h4>
               <p>It needs to be filled within 15 days of passing the special resolution with some attachment</p>
            </div>
            <div class="new_h3">
               <h4>Attachments to Form INC. 27</h4>
               <div class="new_h3_ul_li new_h3_ul_li99">
                  <ul>
                     <li>Certified copy of the passed Resolution of EGM</li>
                     <li>MOA Amended</li>
                     <li>AOA Amended</li>
                     <li>Notice of shorter EGM, if it is held after a short notice</li>
                     <li>Minutes of General Meeting of the Members</li>
                     <li>Getting Approval for MGT-14 and INC-27</li>
                     <li>These two forms and the attached documents must be approved by the
                        ROC (Register of Companies).</li>
                  </ul>
               </div>
            </div>
            <div class="new_h3">
               <h4>Certificate of Incorporation from ROC</h4>
               <p>As soon as the forms MGT 14 and INC 27 are approved by the ROC, the former company incorporation will be canceled and a new certificate of company incorporation will be provided against the private limited company.</p>
            </div>
