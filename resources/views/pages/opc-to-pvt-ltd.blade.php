                  </ul>
               </div>
               <div class="new_h3_inner">
                  <h5>Compliance with Section 18</h5>
                  <p>The conversion of OPC to Pvt. Ltd. Company must follow the rules of section 18 of the Companies Act 2013. Then, your company will provide relevant legal requirements for fulfilling the compliance of a Pvt. Ltd. company.</p>
               </div>
