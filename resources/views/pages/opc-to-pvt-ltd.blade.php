               <p>The Memorandum of Association (MoA) and the Article of Association (AoA) of one Person Company have to be altered to the following section of 122(3) of the Companies Act, 2013. These changes must be conducted and implemented during the process of company conversion under Section 18/1 of the Companies Act and the rule of 6(1) of the Company Incorporation Rule of 2014.</p>
            </div>
            <div class="new_h3">
               <h4>Minimum Requirement Compliance</h4>
               <p>While converting OPC to Pvt. Ltd. Company or a Public Limited Company (as the case may be), you must adhere to the minimum requirements of the conversion in rule 6(2) of the Company Incorporation Rule, 2014.</p>
