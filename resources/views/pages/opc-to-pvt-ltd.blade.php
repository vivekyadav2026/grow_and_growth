                     <li>Notice for Meeting of Board of Directors</li>
                     <li>A meeting with the board of directors is arranged, and it must be conducted before 7 days of the meeting.</li>
                     <li>The Board of Meeting Resolution</li>
                     <li>The meeting would be for the Appointment of directors</li>
                     <li>The date time and venue of the EOGM or Extra-Ordinary General Meeting must be declared</li>
                     <li>Approval of draft notice of EOGM, following Section 102 of the Companies Act 2013.</li>
                     <li>Approval of the MoA, and AoA</li>
                     <li>Authorization of Company secretary or director</li>
                     <li>File the required form INC-5 and submit it to ROC (Register of Companies)</li>
                  </ul>
