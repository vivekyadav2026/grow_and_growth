         <p class="two_p">There are two distinctive ways through which an OPC can be converted into Pvt. Ltd. Company. They are:</p>
         <p>1. Voluntary conversion</p>
         <p>2. Compulsory conversion</p>
      </div>
      <div class="new_block_section">
         <h4>Clarification of Voluntary and Compulsory Conversion</h4>
         <div class="new_block_section_block">
            <h5>1. Voluntary Conversion</h5>
            <p>A voluntary conversion to a private limited company is not permitted before the completion of 2 years as One Person Company from the day of incorporation. The next term for voluntary conversion is financial turnover. If the One Person Company’s paid-up share is more than Rs. 50 lakhs or the annual turnover is Rs 2 crores, the company will automatically turn into a voluntary Pvt Ltd Company within 2 months. Once the company fills up the mentioned criteria and converts into a private limited company, The OPC authority needs to inform the ROC (Register of Companies) through the form INC-5 within the next 60 days of voluntary conversion.</p>
         </div>
         <div class="new_block_section_block">
            <h5>2. Mandatory or Compulsory Conversion</h5>
            <p>If the circumstances of the OPC are like the following, the authority can convert it to Pvt. Ltd. Company.</p>
            <p>1. The paid-up share capital crosses the capital of Rs 50 lakhs</p>
            <p>2. The average turnover of the last 3 years of consecutive financial years exceeds more than 2 crores.</p>
            <p>When these two terms are fulfilled, the company authority can apply for the private limited company conversion in the next 6 months.</p>
         </div>
