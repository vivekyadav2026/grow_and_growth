$dir = __DIR__ . '/resources/views/pages';

// 1. Rename files
foreach ($map as $old => $new) {
    $oldPath = $dir . '/' . $old . '.blade.php';
    $newPath = $dir . '/' . $new . '.blade.php';
    if (file_exists($oldPath)) {
        rename($oldPath, $newPath);
    }
}

// 2. Build replacements for internal HTML links
// We need to replace href="Old Name.html" with href="/new-name"
// Since the old links in the HTML might be URL encoded or have exact casing, 
// we'll do a generic str_ireplace for the old filenames.

// The original filenames were the keys but with exact casing. 
// We'll read the directory to find the new files.
$files = glob($dir . '/*.blade.php');

foreach ($files as $file) {
    $content = file_get_contents($file);
    
    // We map both the slugified names AND the original strings
    foreach ($map as $old => $new) {
        $content = str_ireplace($old . '.html', '/' . $new, $content);
        // Also sometimes the links might use URL encoding for spaces:
        // "12A Registration _ 80G Registration.html"
        // Let's create the unslugified guess to replace it, but it's hard.
        // It's safer to use regex to find all <a href="..."> and see if it ends with .html
    }

    // Advanced Regex for href="some-page.html"
    $content = preg_replace_callback('/href\s*=\s*["\']([^"\']+\.html)["\']/i', function($matches) use ($map) {
        $link = urldecode($matches[1]);
        $basename = basename($link, '.html');
        $slug = strtolower(preg_replace('/[^a-zA-Z0-9]+/', '-', $basename));
        
        if (isset($map[$slug])) {
            return 'href="/' . $map[$slug] . '"';
        }
        return $matches[0];
    }, $content);

    file_put_contents($file, $content);
}

